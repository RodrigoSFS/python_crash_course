# Listas de duas dimenções

drinks = ["coffe", "soda", "tea"]
dinner = ["pizza", "hamburguer", "hotdog"]
dessert = ["cake", "ice cream"]

# para fazer listas bidimensionais, basta fazer uma lista que contém listas
food = [drinks, dinner, dessert]

# para acessar as litas, basta usar o index desejado [] []
#print(food [0])

print(food [0][0])
print(food [0][1])
print(food [0][2])
print(food [1][2])